function widthSearch(){
    let divSearch = document.getElementsByClassName("divSearch")
    let width_search = document.getElementById("pesquisa")
    divSearch[0].style.width='20%'
    width_search.style.backgroundColor='white'
}

function widthSearchX(){
    let divSearch = document.getElementsByClassName("divSearch")
    let width_search = document.getElementById("pesquisa")
    divSearch[0].style.width=''
    width_search.style.backgroundColor=''
}